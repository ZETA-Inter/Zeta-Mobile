package com.example.feature_produtor.model;


public class Content {

    private String type;

    private String data;

    public Content() {}
    // Getters e Setters


    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}