package com.example.feature_produtor.model;

public class Segment {

    private Integer id;
    private String name;


    public Segment() {
    }


    // Getters e Setters
    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
